const CATEGORIES = [
  "behavior-only",
  "guarantees-stressed",
  "error-condition-stressed",
  "non-functional-stressed",
  "multi-aspect",
] as const;

type Category = (typeof CATEGORIES)[number];

interface CategoryMetrics {
  readonly category: Category;
  readonly entries: number;
  readonly M1: number;
  readonly M2: number;
  readonly M3: number;
  readonly M4: number;
}

function computePerCategoryMetrics(
  entries: readonly StratifiedEntry[],
  results: Awaited<ReturnType<typeof runBenchmarkEntries>>,
): readonly CategoryMetrics[] {
  return CATEGORIES.map((cat) => {
    const catIndices = entries
      .map((e, i) => ({ e, i }))
      .filter(({ e }) => e.category === cat)
      .map(({ i }) => i);
    const catResults = catIndices.map((i) => results[i]).filter((r) => r !== undefined);
    return {
      category: cat,
      entries: catResults.length,
      M1: computeHitRate(catResults),
      M2: computePrecisionAt1(catResults),
      M3: computeRecallAtK(catResults),
      M4: computeMRR(catResults),
    };
  });
}

