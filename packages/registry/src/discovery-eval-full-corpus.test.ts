/** SHA-256 of the corpus file bytes, for determinism anchoring in baseline artifacts. */
function corpusHash(): string {
  const bytes = readFileSync(CORPUS_PATH);
  return createHash("sha256").update(bytes).digest("hex").slice(0, 16);
}

